// Control Flow
/*
    Control flow is the order in which the computer executes statements in a script.
    Flow is generally begining to end one line at a time.
*/

// JavaScript Comparision and Logical Operators
/*
    Comparison operator compare any two values or expression returning boolean output.
    Logical operator returns boolean value by evaluating boolean expressions.
*/
let x=5;
let y=10;
console.log("Comparision operators");
console.log("x>y",x>y);
console.log("x<y",x<y);
console.log("x==y",x==y);
console.log("x!=y",x!=y);
console.log("x>=y",x>=y);
console.log("x<=y",x<=y);

y="5"
console.log("x===y",x===y); // strictly check data type as well as value.

console.log("\nLogical Operator");
x= true;
y=false; 
console.log("x&&y",x&&y);
console.log("x||y",x||y);
console.log("!x",!x);
